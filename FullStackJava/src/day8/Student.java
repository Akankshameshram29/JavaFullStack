package day8;

public class Student {
   private String name ;
   private int age;
   private String gender;
   
   public void setName(String name_) {
	   name = name_;
   }
   public void setAge(int Age_) {
	   age = Age_;
   }
   public void setGender(String gender_) {
	  gender = gender_;
   }
   
   public String getName() {
	   return name;
   }
   public String getGender() {
	   return gender;
   }
   public int getAge() {
	   return age;
   }
   
   
   
}
